// Given a binary array nums, return the maximum number of consecutive 1's in the array.



class Solution {
    public int findMaxConsecutiveOnes(int[] nums) {
        int max=0;
        int current=0;
        for(int i=0;i<nums.length;i++){
            if(nums[i]==1){
                current++;
            }
            else{
                max=Math.max(current,max);
                current=0;
            }
        }
        max=Math.max(current,max);

        return max;
    }
}